# Variáveis podem ter seus valores modificados
i = 5
print(i)
i = i +1
print(i)

# É possível criar strigs com múltiplas linhas ao usar três aspas (simples ou duplas)
s = """Esta é uma frase formada por:
várias linhas."""
print(s)

# Caso você possua uma instrução muito longa, é possível fazer a quebra de linha mantendo a instrução com o uso da barra invertida "\"
i = \
85
print(i)